def parse_supplier_data(record: dict) -> Dict[str, Any]:
    """–ü–∞—Ä—Å–∏–Ω–≥ –¥–∞–Ω–Ω—ã—Ö –ø–æ—Å—Ç–∞–≤—â–∏–∫–∞"""
    order_days = []
    for key in ['–î–µ–Ω—å –≤—ã—Ö–æ–¥–∞ –∑–∞–∫–∞–∑–∞', '–î–µ–Ω—å –≤—ã—Ö–æ–¥–∞ –∑–∞–∫–∞–∑–∞ 2', '–î–µ–Ω—å –≤—ã—Ö–æ–¥–∞ –∑–∞–∫–∞–∑–∞ 3']:
        value = str(record.get(key, '')).strip()
        if value and value.isdigit():
            order_days.append(int(value))
    
    delivery_days = str(record.get('–°—Ä–æ–∫ –¥–æ—Å—Ç–∞–≤–∫–∏ –≤ –º–∞–≥–∞–∑–∏–Ω', '0')).strip()
    return {
        'supplier_id': str(record.get('–ù–æ–º–µ—Ä –æ—Å–Ω. –ø–æ—Å—Ç.', '')),
        'order_days': sorted(list(set(order_days))),
        'delivery_days': int(delivery_days) if delivery_days.isdigit() else 0
    }

def calculate_delivery_date(supplier_data: dict) -> Tuple[str, str]:
    """–†–∞—Å—á–µ—Ç –¥–∞—Ç—ã –¥–æ—Å—Ç–∞–≤–∫–∏"""
    today = datetime.now()
    current_weekday = today.isoweekday()

    # –ù–∞—Ö–æ–¥–∏–º –±–ª–∏–∂–∞–π—à–∏–π –¥–µ–Ω—å –∑–∞–∫–∞–∑–∞
    nearest_day = None
    for day in sorted(supplier_data['order_days']):
        if day >= current_weekday:
            nearest_day = day
            break
    if not nearest_day:
        nearest_day = min(supplier_data['order_days'])
    
    delta_days = (nearest_day - current_weekday) % 7
    order_date = today + timedelta(days=delta_days)
    delivery_date = order_date + timedelta(days=supplier_data['delivery_days'])
    
    return (
        order_date.strftime("%d.%m.%Y"),
        delivery_date.strftime("%d.%m.%Y")
    )


IMAGE_PROCESSING_SEMAPHORE = asyncio.Semaphore(MAX_WORKERS)


async def process_barcode_image(photo: types.PhotoSize) -> Tuple[Optional[str], Optional[str]]:
    """–£–ª—É—á—à–µ–Ω–Ω–∞—è –æ–±—Ä–∞–±–æ—Ç–∫–∞ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π —Å–æ —à—Ç—Ä–∏—Ö-–∫–æ–¥–æ–º"""
    start_time = time.monotonic()
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫—ç—à–∞ —à—Ç—Ä–∏—Ö-–∫–æ–¥–æ–≤
    if "barcodes_index" not in cache or not cache["barcodes_index"]:
        return None, "–°–∏—Å—Ç–µ–º–∞ —à—Ç—Ä–∏—Ö-–∫–æ–¥–æ–≤ –Ω–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∞"

    # –í—ã–±–∏—Ä–∞–µ–º —Ñ–æ—Ç–æ –Ω–∞–∏–ª—É—á—à–µ–≥–æ –∫–∞—á–µ—Å—Ç–≤–∞
    best_photo = max(photo, key=lambda p: p.width * p.height)
    
    try:
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ä–∞–∑–º–µ—Ä —Ñ–∞–π–ª–∞ –ø–µ—Ä–µ–¥ —Å–∫–∞—á–∏–≤–∞–Ω–∏–µ–º
        file_info = await bot.get_file(best_photo.file_id)
        if file_info.file_size > MAX_IMAGE_SIZE:
            return None, f"–ò–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ —Å–ª–∏—à–∫–æ–º –±–æ–ª—å—à–æ–µ ({file_info.file_size//1024}KB > {MAX_IMAGE_SIZE//1024}KB)"

        # –°–∫–∞—á–∏–≤–∞–µ–º –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ —á–∞—Å—Ç—è–º–∏ —Å –ø—Ä–æ–≤–µ—Ä–∫–æ–π —Ä–∞–∑–º–µ—Ä–∞
        image_bytes = b""
        async with IMAGE_PROCESSING_SEMAPHORE:
            file = await bot.get_file(photo.file_id)
            async for chunk in file.download(destination=None):
                if len(image_bytes) + len(chunk) > MAX_IMAGE_SIZE:
                    return None, "–ü—Ä–µ–≤—ã—à–µ–Ω –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–π —Ä–∞–∑–º–µ—Ä –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è"
                image_bytes += chunk

        # –û–±—Ä–∞–±–æ—Ç–∫–∞ –≤ –æ—Ç–¥–µ–ª—å–Ω–æ–º –ø–æ—Ç–æ–∫–µ
        loop = asyncio.get_running_loop()
        decoded_objects = None
    
        def process_image():
            """–°–∏–Ω—Ö—Ä–æ–Ω–Ω–∞—è –æ–±—Ä–∞–±–æ—Ç–∫–∞ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è"""
            nonlocal decoded_objects
            try:
                # –û–ø—Ç–∏–º–∏–∑–∞—Ü–∏—è –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è –ø–µ—Ä–µ–¥ —Ä–∞—Å–ø–æ–∑–Ω–∞–≤–∞–Ω–∏–µ–º
                with io.BytesIO(image_bytes) as buffer:
                    with Image.open(buffer) as img:
                        # –ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ–º –≤ grayscale –¥–ª—è —É–ª—É—á—à–µ–Ω–∏—è —Ä–∞—Å–ø–æ–∑–Ω–∞–≤–∞–Ω–∏—è
                        img = img.convert('L')
                        
                        # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º –∫–æ–Ω—Ç—Ä–∞—Å—Ç
                        enhancer = ImageEnhance.Contrast(img)
                        img = enhancer.enhance(2.0)
                        
                        # –ú–∞—Å—à—Ç–∞–±–∏—Ä—É–µ–º, —Å–æ—Ö—Ä–∞–Ω—è—è –ø—Ä–æ–ø–æ—Ä—Ü–∏–∏
                        scale_factor = 800 / max(img.width, img.height)
                        new_size = (int(img.width * scale_factor), int(img.height * scale_factor))
                        img = img.resize(new_size, Image.LANCZOS)
                        
                        # –†–∞—Å–ø–æ–∑–Ω–∞–µ–º —à—Ç—Ä–∏—Ö-–∫–æ–¥—ã
                        return decode(img)
            except Exception as e:
                logging.error(f"–û—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è: {str(e)}")
                return None
    
        # –í—ã–ø–æ–ª–Ω—è–µ–º –≤ thread pool —Å —Ç–∞–π–º–∞—É—Ç–æ–º
        decoded_objects = await asyncio.wait_for(
            loop.run_in_executor(image_processor, process_image),
            timeout=15.0
        )
        
        if not decoded_objects:
            return None, "–®—Ç—Ä–∏—Ö-–∫–æ–¥ –Ω–µ —Ä–∞—Å–ø–æ–∑–Ω–∞–Ω"

        # –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –≤—Å–µ –Ω–∞–π–¥–µ–Ω–Ω—ã–µ —à—Ç—Ä–∏—Ö-–∫–æ–¥—ã
        barcodes_index = pickle.loads(cache["barcodes_index"])
        for obj in decoded_objects:
            try:
                barcode_data = obj.data.decode("utf-8").strip()
                if article := barcodes_index.get(barcode_data):
                    return article, None
            except UnicodeDecodeError:
                # –ü—Ä–æ–±—É–µ–º –∞–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–Ω—ã–µ –∫–æ–¥–∏—Ä–æ–≤–∫–∏
                for encoding in ['latin-1', 'cp1251']:
                    try:
                        barcode_data = obj.data.decode(encoding).strip()
                        if article := barcodes_index.get(barcode_data):
                            return article, None
                    except:
                        continue
        
        return None, "–†–∞—Å–ø–æ–∑–Ω–∞–Ω–Ω—ã–π —à—Ç—Ä–∏—Ö-–∫–æ–¥ –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ –±–∞–∑–µ"
    
    except asyncio.TimeoutError:
        return None, "–ü—Ä–µ–≤—ã—à–µ–Ω–æ –≤—Ä–µ–º—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è"
    except TelegramBadRequest as e:
        logging.error(f"–û—à–∏–±–∫–∞ Telegram: {str(e)}")
        return None, "–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ —Ñ–∞–π–ª–∞"
    except Exception as e:
        logging.exception(f"–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏: {str(e)}")
        return None, "–û—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—è"
    finally:
        duration = time.monotonic() - start_time
        logging.info(f"Barcode processing took {duration:.2f} seconds")
        # –ü—Ä–∏–Ω—É–¥–∏—Ç–µ–ª—å–Ω–∞—è –æ—á–∏—Å—Ç–∫–∞ –ø–∞–º—è—Ç–∏
        del image_bytes
        gc.collect()


